#include "libft_ll.h"
#include <stdlib.h>

void	ft_lstadd_back(t_list **lst, t_list *new_l)
{
	if (*lst == NULL)
		*lst = new_l;
	else
		ft_lstlast(*lst)->next = new_l;
}
